def detect_language() -> str:
    selected = session.get("lang")
    if selected in SUPPORTED_LANGUAGES:
        return selected

    best = request.accept_languages.best_match(SUPPORTED_LANGUAGES)
    return best if best in SUPPORTED_LANGUAGES else "en"


def translate(lang: str, key: str, **kwargs) -> str:
    text = TRANSLATIONS.get(lang, TRANSLATIONS["en"]).get(key)
    if text is None:
        text = TRANSLATIONS["en"].get(key, key)
    return text.format(**kwargs) if kwargs else text
